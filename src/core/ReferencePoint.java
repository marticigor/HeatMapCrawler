package core;

public class ReferencePoint {

 @SuppressWarnings("unused")
private int x;
 @SuppressWarnings("unused")
private int y;
 @SuppressWarnings("unused")
private double lon;
 @SuppressWarnings("unused")
private double lat;

 public ReferencePoint(int x, int y, double lon, double lat) {

  this.x = x;
  this.y = y;
  this.lon = lon;
  this.lat = lat;

 }
}
